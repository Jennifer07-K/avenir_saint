            <div class="faq-item faq-active">
              <h3>Qu'est-ce que la drépanocytose ?</h3>
              <div class="faq-content">
                <p>La drépanocytose est une maladie génétique du sang qui affecte l'hémoglobine, provoquant une forme anormale des globules rouges qui peuvent bloquer la circulation sanguine et causer des douleurs intenses.</p>
              </div>
              <i class="faq-toggle bi bi-chevron-right"></i>
            </div><!-- End Faq item-->
  
            <div class="faq-item">
              <h3>Quels sont les symptômes les plus courants ?</h3>
              <div class="faq-content">
                <p>Les symptômes incluent des douleurs aiguës (crises drépanocytaires), une anémie, une fatigue chronique, des infections fréquentes et un risque accru de complications graves.</p>
              </div>
              <i class="faq-toggle bi bi-chevron-right"></i>
            </div><!-- End Faq item-->
  
            <div class="faq-item">
              <h3>Comment se transmet la drépanocytose ?</h3>
              <div class="faq-content">
                <p>Elle est héréditaire et transmise par les deux parents s'ils sont porteurs du gène drépanocytaire. Un enfant peut être atteint s'il hérite de deux copies du gène muté.</p>
              </div>
              <i class="faq-toggle bi bi-chevron-right"></i>
            </div><!-- End Faq item-->
  
            <div class="faq-item">
              <h3>Existe-t-il un traitement ?</h3>
              <div class="faq-content">
                <p>Il n'existe pas encore de remède universel, mais des traitements comme l'hydroxyurée, les transfusions sanguines et la greffe de moelle osseuse peuvent aider à gérer la maladie.</p>
              </div>
              <i class="faq-toggle bi bi-chevron-right"></i>
            </div><!-- End Faq item-->
  
            <div class="faq-item">
              <h3>Comment vivre avec la drépanocytose ?</h3>
              <div class="faq-content">
                <p>Une bonne hydratation, une alimentation équilibrée, un suivi médical régulier et l'évitement des facteurs déclencheurs comme le stress et le froid peuvent améliorer la qualité de vie.</p>
              </div>
              <i class="faq-toggle bi bi-chevron-right"></i>
            </div><!-- End Faq item-->
  
            <div class="faq-item">
              <h3>Où puis-je me faire dépister ?</h3>
              <div class="faq-content">
                <p>Le dépistage est disponible dans de nombreux centres de santé et hôpitaux. Consultez un professionnel de santé pour en savoir plus sur les tests disponibles près de chez vous.</p>
